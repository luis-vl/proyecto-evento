    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        // TODO add your handling code here:
        if (txtUsuario.getText().trim().equals("") || txtContraseña.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Asegúrese de ingresar corretamente sus credenciales");
            return;
        }

        try {
            Session session = instancia.openSession();
            String str = txtUsuario.getText().trim();
            
            Criteria criteria = session.createCriteria(Users.class).add(Restrictions.eq("usuario", str));
            Users u = (Users) criteria.list().get(0);
            
            //System.out.println(txtUsuario.getText() + " - " + txtContraseña.getPassword());
            
            if (txtUsuario.getText().trim().equals(u.getUsuario()) && txtContraseña.getText().equals(u.getPass())) {
                Principal p = new Principal();
                p.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Usuario o Contraseña incorrecta...", "Error de Credenciales", JOptionPane.ERROR_MESSAGE);
            }

        } catch (HeadlessException | HibernateException e) {
            JOptionPane.showConfirmDialog(null, e);
        }


    }//GEN-LAST:event_btnIniciarSesionActionPerformed

