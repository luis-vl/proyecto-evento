    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        if (txtTipoMantenimiento.getText().isEmpty() || listEmpresa.getSelectedIndex() < 0 || listSupervisor.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(this, "No deje campos vacios");
            return;
        }
        if (edit) {
            mantenimiento = mant.getByID(mantenimiento.getIdMantenimiento());
            getEditCampos();
            mant.update(mantenimiento);
        } else {
            getCampos();
            mant.add(getMantenimiento());
            this.dispose();
        }

        this.dispose();
    }//GEN-LAST:event_btnOKActionPerformed

    private void txtTipoMantenimientoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTipoMantenimientoKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();

        if (Character.isDigit(c)) {
            getToolkit().beep();

            evt.consume();

            JOptionPane.showMessageDialog(null, "Ingrese solo letras");

        }
    }//GEN-LAST:event_txtTipoMantenimientoKeyTyped

