    private void getCampos() {
        Mantenimiento m = new Mantenimiento();
        m.setSalon(salon.getAt(cbxSalon.getSelectedIndex()));
        m.setEmpresa(empresa.getAt(listEmpresa.getSelectedIndex()));
        m.setSupervisor(supervisor.getAt(listSupervisor.getSelectedIndex()));
        m.setTipoMantenimiento(txtTipoMantenimiento.getText());
        m.setCosto((Float) spCosto.getValue());
        m.setFechaRegistro(new Date());
        setMantenimiento(m);
    }

    private void getEditCampos() {
        mantenimiento.setSalon(salon.getAt(cbxSalon.getSelectedIndex()));
        mantenimiento.setEmpresa(empresa.getAt(listEmpresa.getSelectedIndex()));
        mantenimiento.setSupervisor(supervisor.getAt(listSupervisor.getSelectedIndex()));
        mantenimiento.setTipoMantenimiento(txtTipoMantenimiento.getText());
        mantenimiento.setCosto((Float) spCosto.getValue());
        mantenimiento.setFechaRegistro(new Date());
    }

    public void setCampos() {
        cbxSalon.setSelectedItem(mantenimiento.getSalon().getNombre());
        txtEmpresa.setText(mantenimiento.getEmpresa().getNombre());
        txtSupervisor.setText(mantenimiento.getSupervisor().getNombre() + " " + mantenimiento.getSupervisor().getApellido());
        txtTipoMantenimiento.setText(mantenimiento.getTipoMantenimiento());
        spCosto.setValue(mantenimiento.getCosto());
    }

